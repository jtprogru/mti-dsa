"""Задача: даны три отсортированных массива (возможно разной длины).
Гарантируется, что хотя бы одно число есть во всех трёх.

Найти: любое общее для всех трёх число.

Пример:
    [1, 2, 3, 4, 5, 100], [6, 7, 8, 9, 100], [10, 20, 21, 100] -> 100

Закрепляет: lab04 (поиск, три указателя). Раз все массивы отсортированы,
двигаем указатель того массива, где сейчас наименьший элемент: совпадение
во всех трёх и будет ответом. Это обобщение слияния отсортированных списков.
Сложность: O(n1 + n2 + n3) время, O(1) память.
"""


def common_in_three(a: list[int], b: list[int], c: list[int]) -> int | None:
    if not a or not b or not c:
        return None
    i = j = k = 0
    while i < len(a) and j < len(b) and k < len(c):
        if a[i] == b[j] == c[k]:
            return a[i]
        # Сдвигаем указатель массива с наименьшим текущим элементом.
        smallest = min(a[i], b[j], c[k])
        if a[i] == smallest:
            i += 1
        elif b[j] == smallest:
            j += 1
        else:
            k += 1
    return None
