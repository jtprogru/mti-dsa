"""Задача (two sum в отсортированном массиве): дан отсортированный массив `a`
и число `k`.

Найти: два числа из массива, дающие в сумме `k`. Если пары нет — вернуть [].

Пример:
    a = [-1, 2, 5, 8], k = 7 -> [-1, 8]   (-1 + 8 == 7; пара берётся с краёв)

Закрепляет: lab01 (два указателя). Раз массив отсортирован, держим указатели
по краям: если сумма мала — двигаем левый вправо (увеличиваем), если велика —
правый влево. Возвращаем первую найденную пару. Это даёт O(n) без
хеш-таблицы и без доп. памяти.
Сложность: O(n) время, O(1) память.
"""


def two_sum_sorted(a: list[int], k: int) -> list[int]:
    left = 0
    right = len(a) - 1
    while left < right:
        current = a[left] + a[right]
        if current == k:
            return [a[left], a[right]]
        if current < k:
            left += 1
        else:
            right -= 1
    return []
