"""Задача (two sum): дан массив целых `nums` и число `target`.

Найти: индексы двух элементов, сумма которых равна `target`. Ровно одно
решение существует; один и тот же элемент дважды использовать нельзя.

Пример:
    nums = [2, 7, 11, 15], target = 9 -> [0, 1]   (nums[0] + nums[1] == 9)
    nums = [3, 2, 4],       target = 6 -> [1, 2]

Закрепляет: lab05 (хеш-таблица). За один проход держим в словаре
«значение -> индекс»; для каждого числа ищем недостающее дополнение за
O(1) вместо вложенного цикла O(n^2).
Сложность: O(n) время, O(n) память.
"""


def two_sum(nums: list[int], target: int) -> list[int]:
    seen: dict[int, int] = {}  # значение -> индекс, на котором оно встретилось
    for i, num in enumerate(nums):
        need = target - num
        if need in seen:
            return [seen[need], i]
        seen[num] = i
    return []
