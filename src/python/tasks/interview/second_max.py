"""Задача (второй максимум): дан несортированный массив `arr`.

Найти: второй по величине **уникальный** элемент за один проход. Если такого
нет (массив короче двух элементов или все значения равны) — вернуть None.

Пример:
    [-2, 3, 0, 1, 5] -> 3
    [1, 1]           -> None   (один уникальный элемент)
    [5, 5, 4]        -> 4

Закрепляет: основы (один проход, инвариант). Держим два «призёра» — самый
большой и второй по величине различающийся — и обновляем их по ходу. В
отличие от `sorted(arr)[-2]` это честный O(n) без сортировки и корректно
работает с дубликатами (второй максимум именно среди РАЗНЫХ значений).
Сложность: O(n) время, O(1) память.
"""


def second_max(arr: list[int]) -> int | None:
    first: int | None = None  # наибольшее значение
    second: int | None = None  # второе по величине, строго меньше first
    for num in arr:
        if first is None or num > first:
            if num != first:  # прежний максимум становится вторым
                second = first
            first = num
        elif num != first and (second is None or num > second):
            second = num
    return second
