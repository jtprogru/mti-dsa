"""Задача (plus one): большое целое задано массивом цифр `digits` от старшего
разряда к младшему, без ведущих нулей.

Найти: прибавить к числу единицу и вернуть массив цифр результата.

Пример:
    [1, 2, 3] -> [1, 2, 4]
    [1, 2, 9] -> [1, 3, 0]
    [9, 9]    -> [1, 0, 0]

Закрепляет: основы (массив, перенос разряда). Идём с конца: если цифра < 9 —
просто увеличиваем и выходим; если 9 — ставим 0 и несём перенос дальше.
Если перенос «вылез» за старший разряд, добавляем 1 в начало. Это ручное
сложение «в столбик» — та же арифметика, что в больших числах.
Сложность: O(n) время, O(1) доп. память (без учёта результата).
"""


def plus_one(digits: list[int]) -> list[int]:
    result = list(digits)
    for i in range(len(result) - 1, -1, -1):
        if result[i] < 9:
            result[i] += 1
            return result
        result[i] = 0  # был 9 -> 0 и несём перенос в старший разряд
    return [1] + result  # перенос вышел за пределы числа: 99 -> 100
