"""Задача (missing number): дан массив, где должны лежать все числа 0..N,
но одно пропущено. Числа неотрицательные и без повторов (порядок любой).

Найти: пропущенное число.

Пример:
    [0, 1, 2, 3, 5] -> 4
    [1, 2, 3, 4, 5] -> 0

Закрепляет: lab01 (массив). Сумма всех чисел 0..N считается по формуле
Гаусса за O(1); вычитаем из неё фактическую сумму массива — разница и есть
пропуск. Никакой сортировки и доп. памяти.
Сложность: O(n) время, O(1) память.
"""


def missing_number(nums: list[int]) -> int:
    n = len(nums)
    expected = n * (n + 1) // 2  # сумма 0..n по формуле арифметической прогрессии
    return expected - sum(nums)
