"""Задача (move zeroes): дан массив целых чисел.

Найти: переместить все нули в конец, сохранив относительный порядок
ненулевых элементов. Делать на месте, без копии массива.

Пример:
    [0, 1, 0, 3, 12] -> [1, 3, 12, 0, 0]
    [0]              -> [0]

Закрепляет: lab01 (массив,in-place, два указателя). Указатель `write`
отмечает, куда положить следующий ненулевой элемент; в конце оставшиеся
позиции добиваем нулями. Один проход, без доп. памяти.
Сложность: O(n) время, O(1) доп. память.
"""


def move_zeroes(nums: list[int]) -> None:
    write = 0
    for num in nums:
        if num != 0:
            nums[write] = num
            write += 1
    for i in range(write, len(nums)):
        nums[i] = 0
