"""Задача (reverse string): дана строка как список символов `s`.

Найти: развернуть её на месте, с O(1) доп. памяти (без срезов и нового
списка).

Пример:
    ["h", "e", "l", "l", "o"] -> ["o", "l", "l", "e", "h"]

Закрепляет: lab01 (два указателя). Меняем местами символы с краёв, сходясь
к центру. Базовый шаблон «двух указателей навстречу», на нём же стоят
проверка палиндрома и разворот подотрезков.
Сложность: O(n) время, O(1) доп. память.
"""


def reverse_string(s: list[str]) -> None:
    left = 0
    right = len(s) - 1
    while left < right:
        s[left], s[right] = s[right], s[left]
        left += 1
        right -= 1
