"""Задача: дана строка, например "aafbaaaaffc".

Найти: для каждого символа — максимальную длину его непрерывной серии
(подряд идущих повторов) в строке.

Пример:
    "aafbaaaaffc" -> {"a": 4, "f": 2, "b": 1, "c": 1}

Закрепляет: lab05 (хеш-таблица). Идём по строке, считаем текущую серию и
обновляем максимум по символу в словаре. Тот же приём — «схлопывание
подряд идущих одинаковых событий» — нужен при дедупликации логов и алертов.
Сложность: O(n) время, O(k) память (k — число уникальных символов).
"""


def max_consecutive_repeats(s: str) -> dict[str, int]:
    if not s:
        return {}

    best: dict[str, int] = {}
    prev = s[0]
    run = 0
    for ch in s:
        if ch == prev:
            run += 1
        else:
            prev = ch
            run = 1
        if run > best.get(ch, 0):
            best[ch] = run
    return best
