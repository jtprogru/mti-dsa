"""Задача (single number): дан непустой массив, где каждый элемент
встречается дважды, кроме одного — он один.

Найти: этот единственный элемент. Требование: линейное время и O(1) памяти.

Пример:
    [2, 2, 1]       -> 1
    [4, 1, 2, 1, 2] -> 4

Закрепляет: основы (битовые операции). XOR числа с самим собой = 0, а с
нулём = само число. Прогоняя XOR по всему массиву, пары взаимно
уничтожаются, и остаётся «одиночка» — без словаря и без доп. памяти.
Сложность: O(n) время, O(1) память.
"""


def single_number(nums: list[int]) -> int:
    result = 0
    for num in nums:
        result ^= num
    return result
