"""Задача (daily temperatures): дан массив температур по дням.

Найти: для каждого дня — через сколько суток наступит более тёплый день
(0, если такого дня впереди нет).

Пример:
    [13, 12, 15, 11, 9, 12, 16] -> [2, 1, 4, 2, 1, 1, 0]

Закрепляет: lab01 (стек). Это «монотонный стек»: храним индексы дней, для
которых ответ ещё не найден, по убыванию температуры. Каждый новый день,
который теплее вершины стека, «закрывает» её — расстояние и есть ответ.
Тот же приём (стек ожидающих событий) встречается в разборе временных рядов.
Сложность: O(n) время, O(n) память.
"""


def daily_temperatures(temperatures: list[int]) -> list[int]:
    n = len(temperatures)
    answer = [0] * n
    stack: list[int] = []  # индексы дней, ждущих более тёплого; температуры убывают
    for day, temp in enumerate(temperatures):
        while stack and temperatures[stack[-1]] < temp:
            prev_day = stack.pop()
            answer[prev_day] = day - prev_day
        stack.append(day)
    return answer
