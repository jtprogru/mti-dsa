"""Задача (remove duplicates from sorted array): дан отсортированный массив.

Найти: удалить повторы на месте (без создания нового массива и без `set`)
и вернуть новую длину — количество уникальных элементов.

Пример:
    [0, 1, 2, 3, 3, 5] -> 5  (массив становится [0, 1, 2, 3, 5])
    [1, 1, 2]          -> 2

Закрепляет: lab01 (массив, in-place). Так как массив отсортирован, дубли
лежат рядом. Указатель `write` отмечает место для следующего уникального
элемента — классический приём «двух указателей» по одному массиву.
Сложность: O(n) время, O(1) доп. память.
"""


def remove_duplicates(nums: list[int]) -> int:
    if not nums:
        return 0
    write = 1  # позиция для следующего уникального элемента
    for read in range(1, len(nums)):
        if nums[read] != nums[write - 1]:
            nums[write] = nums[read]
            write += 1
    del nums[write:]  # отбрасываем «хвост» с дубликатами
    return write
